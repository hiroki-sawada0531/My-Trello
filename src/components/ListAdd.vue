<template>
    <form class="addlist" @submit.prevent="addList">
        <input v-module = "title"
            type = "text"
            class = "text-input"
            placeholder = "Add new list"
        >
        <button type="submit" class="add-button">
            Add
        </button>
    </form>
</template>

<script>
export default {
    data: function() {
        return {
            title:"",
        }
    },

    methods: {
    addList:function() {
        this.$store.dispach('addlist', { title: this.title })
        this.title = ""
    },
}

}
</script>

